for (const [lang, translations] of Object.entries(locales)) {
    const file = path.join(__dirname, 'lib/i18n/locales', `${lang}.ts`);
    if (fs.existsSync(file)) {
        let content = fs.readFileSync(file, 'utf8');
        
        // Match the entire overview: { ... } block
        const overviewRegex = /overview:\s*{([\s\S]*?)},\s*common:/;
        const match = content.match(overviewRegex);
        
        if (match) {
            let blockContent = match[1];
            
            // For each key in translations, update or append it inside the overview block
            for (const [key, val] of Object.entries(translations)) {
                // escape single quotes
                const escapedVal = val.replace(/'/g, "\\'");
                const keyRegex = new RegExp(`${key}:\\s*['\`].*?['\`],`, 'g');
                
                if (keyRegex.test(blockContent)) {
                    blockContent = blockContent.replace(keyRegex, `${key}: '${escapedVal}',`);
                } else {
                    blockContent = blockContent.replace(/\s*$/, `\n        ${key}: '${escapedVal}',\n`);
                }
            }
            
            content = content.replace(overviewRegex, `overview: {${blockContent}    },\n    common:`);
            fs.writeFileSync(file, content);
            console.log(`Updated overview translations for ${lang}.ts`);
        }
    }
}
