-- Basic Data Access Policies
CREATE POLICY "Users can view their own profile" 
ON public.profiles FOR SELECT USING (auth.uid() = id);

CREATE POLICY "Partners view their own links" 
ON public.referral_links FOR SELECT USING (partner_id = auth.uid());

CREATE POLICY "Partners view their own clicks" 
ON public.clicks FOR SELECT USING (partner_id = auth.uid());

CREATE POLICY "Partners view their own leads" 
ON public.leads FOR SELECT USING (partner_id = auth.uid());

CREATE POLICY "Partners view their own commissions" 
ON public.commissions FOR SELECT USING (partner_id = auth.uid());

CREATE POLICY "Partners view their own withdrawals" 
ON public.withdrawals FOR SELECT USING (partner_id = auth.uid());
