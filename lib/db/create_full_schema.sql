-- 2. REFERRAL LINKS (Campaigns)
-- Stores the custom tracking links generated by the partner
CREATE TABLE public.referral_links (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    partner_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE,
    campaign_name TEXT NOT NULL,
    url TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 3. CLICKS (Traffic Analytics)
-- Logs raw visits/clicks on the referral links or landings
CREATE TABLE public.clicks (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    partner_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE,
    link_id UUID REFERENCES public.referral_links(id) ON DELETE SET NULL,
    ip_address TEXT,
    device_type TEXT, -- mobile, desktop, tablet
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 4. LEADS (Registered Clients)
-- Clients who registered under the partner
CREATE TABLE public.leads (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    partner_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE,
    link_id UUID REFERENCES public.referral_links(id) ON DELETE SET NULL,
    name TEXT NOT NULL,
    email TEXT NOT NULL,
    whatsapp TEXT,
    status TEXT DEFAULT 'registered' CHECK (status IN ('registered', 'funded', 'trading')),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 5. COMMISSIONS (Earnings)
-- Tracks the amount of money earned by the partner per lead
CREATE TABLE public.commissions (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    partner_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE,
    lead_id UUID REFERENCES public.leads(id) ON DELETE SET NULL,
    amount NUMERIC(10, 2) NOT NULL DEFAULT 0.00,
    type TEXT CHECK (type IN ('CPA', 'RevShare', 'Bonus')),
    status TEXT DEFAULT 'pending' CHECK (status IN ('pending', 'approved', 'paid', 'rejected')),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 6. WITHDRAWALS (Payout Requests)
-- Tracks partner requests to cash out their earnings
CREATE TABLE public.withdrawals (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    partner_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE,
    amount NUMERIC(10, 2) NOT NULL,
    payment_method TEXT NOT NULL, -- e.g. 'crypto_usdt', 'bank_transfer'
    status TEXT DEFAULT 'pending' CHECK (status IN ('pending', 'processing', 'completed', 'rejected')),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- ==========================================
--  INDEXES FOR PERFORMANCE
-- ==========================================
CREATE INDEX idx_profiles_partner_id ON public.profiles(partner_id);
CREATE INDEX idx_referral_links_partner_id ON public.referral_links(partner_id);
CREATE INDEX idx_clicks_partner_id ON public.clicks(partner_id);
CREATE INDEX idx_leads_partner_id ON public.leads(partner_id);
CREATE INDEX idx_commissions_partner_id ON public.commissions(partner_id);
CREATE INDEX idx_withdrawals_partner_id ON public.withdrawals(partner_id);

